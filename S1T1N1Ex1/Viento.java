package S1T1N1Ex1;

public class Viento extends Instrumento{
	
//	bloque estatico
	static{
		System.out.println("Los instrumentos de viento se preparan para tocar.");
	}
	
//	bloque inicialización
	{
		System.out.println("Añadimos un instrumento de viendo a la banda.");
	}
	
	//Constructor
	public Viento(String nombre, int precio){
		super (nombre, precio);
	}
	
	//Llamada método abstracto
	public String tocar() {
		return "Se está tocando un instrumento de viento";
	}
}
